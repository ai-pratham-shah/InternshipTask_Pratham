
# Mapping Type - dict



# Numeric - int,float,complex

a = 5
print("This is integer:", a)
print(type(a))

b = 5.1
print("This is float:", b)
print(type(b))

c = 2 + 1j
print("This is complex type:", c)
print(type(c))

# Sequence Type - string,list,tuple

s = "Aktiv Software"
print("This is string:", s)
print(type(s))

l = [10,20,3]
print("This is list:", l)
print(type(l))

t = (1,2,3)
print("This is tuple:", t)
print(type(t))

# Boolean - bool

print(type(True))

# Set Type - set,frozenset

s1 = set("Aktiv Software")
print("This is a set", s1)
print(type(s1))

s2 = frozenset("This is Aktiv")
print("This is a frozenset", s2)
print(type(s2))


